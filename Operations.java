public class Operations {
    public static void main(String[] args) {
  
        int intValue = 67;
        double doubleValue = 7.5;
        float floatValue = 5.39f;
        double result1 = intValue + doubleValue; 
        double result2 = doubleValue * floatValue; 
        float result3 = floatValue / intValue;
        System.out.println("Result of int + double: " + result1);
        System.out.println("Result of double * float: " + result2);
        System.out.println("Result of float / int: " + result3);
    }
}
